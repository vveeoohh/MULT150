//  improving the game
I would think the game is somewhat easy because even though it is hard to get the balls into their respective goals, there is no risk in not doing so.
I think having a timer would make the game more of a challenge.
Something that would give it a "wow" factor would be some effects when you hit a ball with the bumper or chaos ball.
The parts of the game that are fun would be when I actually hit the ball with the bumper. The tedious parts would be when the balls would be just bouncing along the walls, and I'm just stuck staring at them until it either enters a goal or I can hit it.

